#ifndef SR850_H
#define SR850_H

/* ------------------------------------------------------------------ */
/*  Зависимости от нижнего уровня                                      */
/* ------------------------------------------------------------------ */

extern int GPIB_SendCommand(int addr, const char *cmd, int len);
extern int GPIB_Read(int addr, char *buffer, int maxlen);

/* ------------------------------------------------------------------ */
/*  Константы                                                           */
/* ------------------------------------------------------------------ */

#define SR850_BUF_SIZE      128    /* Размер буфера для ответов GPIB     */
#define SR850_DEFAULT_ADDR   8    /* GPIB-адрес по умолчанию            */

/* Коды параметров OUTP? (из руководства SR850) */
#define SR850_OUTP_X        1
#define SR850_OUTP_Y        2
#define SR850_OUTP_R        3
#define SR850_OUTP_THETA    4

/* ------------------------------------------------------------------ */
/*  Инициализация                                                       */
/* ------------------------------------------------------------------ */

/*
 * InitSR850 — сброс прибора и базовая настройка.
 * Вызывать один раз в начале программы.
 *
 * addr — GPIB-адрес прибора (обычно 8)
 */
void InitSR850(int addr);

/* ------------------------------------------------------------------ */
/*  Основные измерения (OUTP?)                                          */
/* ------------------------------------------------------------------ */

/*
 * GetXSR850     — канал X (OUTP? 1), В
 * GetYSR850     — канал Y (OUTP? 2), В
 * GetRSR850     — амплитуда R (OUTP? 3), В
 * GetThetaSR850 — фаза θ (OUTP? 4), градусы
 *
 * Возвращают 0.0f при ошибке чтения.
 */
float GetXSR850(int addr);
float GetYSR850(int addr);
float GetRSR850(int addr);
float GetThetaSR850(int addr);

/* ------------------------------------------------------------------ */
/*  Курсор                                                              */
/* ------------------------------------------------------------------ */

/*
 * GetCURSSR850 — позиция курсора (CURS? n).
 *
 * cursor — номер курсора (1 или 2)
 * x      — указатель на координату X курсора (выходной)
 * y      — указатель на координату Y курсора (выходной)
 *
 * Возвращает 0 при успехе, -1 при ошибке.
 */
int GetCURSSR850(int addr, int cursor, float *x, float *y);

/* ------------------------------------------------------------------ */
/*  Вспомогательные Get-функции                                         */
/* ------------------------------------------------------------------ */

/*
 * GetFreqSR850  — опорная частота (FREQ?), Гц
 * GetPhaseSR850 — опорная фаза (PHAS?), градусы
 * GetSensSR850  — код чувствительности (SENS?)
 * GetOFLTSR850  — код постоянной времени (OFLT?)
 * GetHARMSR850  — номер гармоники (HARM?)
 */
float GetFreqSR850(int addr);
float GetPhaseSR850(int addr);
int   GetSensSR850(int addr);
int   GetOFLTSR850(int addr);
int   GetHARMSR850(int addr);

/* ------------------------------------------------------------------ */
/*  Вспомогательные Set-функции                                         */
/* ------------------------------------------------------------------ */

/*
 * SetFreqSR850  — установить частоту, Гц
 * SetPhaseSR850 — установить фазу, градусы
 * SetSensSR850  — установить чувствительность (код 0..26)
 * SetOFLTSR850  — установить постоянную времени (код 0..19)
 * SetHARMSR850  — установить номер гармоники (1..19999)
 *
 * Возвращают 0 при успехе (результат GPIB_SendCommand).
 */
int SetFreqSR850(int addr, float freq);
int SetPhaseSR850(int addr, float phase);
int SetSensSR850(int addr, int sens);
int SetOFLTSR850(int addr, int oflt);
int SetHARMSR850(int addr, int harm);

/* ------------------------------------------------------------------ */
/*  Управляющие команды                                                 */
/* ------------------------------------------------------------------ */

/*
 * AutoGainSR850  — автоподстройка усиления (AGAN)
 * AutoPhaseSR850 — автоподстройка фазы (APHS)
 * AutoRangeSR850 — автовыбор диапазона (ARSV)
 * AutoScaleSR850 — автомасштаб (ASCL)
 * ResetSR850     — сброс настроек (*RST)
 * ClearSR850     — очистка статуса (*CLS)
 *
 * Возвращают результат GPIB_SendCommand.
 */
int AutoGainSR850(int addr);
int AutoPhaseSR850(int addr);
int AutoRangeSR850(int addr);
int AutoScaleSR850(int addr);
int ResetSR850(int addr);
int ClearSR850(int addr);

#endif /* SR850_H */