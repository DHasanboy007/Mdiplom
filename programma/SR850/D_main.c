/*
 * main.c
 * Пример использования драйвера SR850
 *
 * Сборка:
 *   gcc -Wall -o demo main.c SR850.c gpib.c -lm
 *
 * gpib.c — уже существующий низкоуровневый модуль проекта.
 */

#include <stdio.h>
#include <unistd.h>     /* sleep() */

#include "SR850.h"

/* GPIB-адрес прибора (DIP-переключатели на задней панели SR850) */
#define ADDR  SR850_DEFAULT_ADDR    /* 8 */

int main(void)
{
    float x, y, r, theta;
    float freq, phase;
    float cx, cy;
    int   sens, oflt;
    int   ret;

    /* ---------------------------------------------------------- */
    /* 1. Инициализация                                             */
    /* ---------------------------------------------------------- */
    printf("Инициализация SR850 (addr=%d)...\n", ADDR);
    InitSR850(ADDR);

    /* ---------------------------------------------------------- */
    /* 2. Чтение текущих параметров                                */
    /* ---------------------------------------------------------- */
    freq  = GetFreqSR850(ADDR);
    phase = GetPhaseSR850(ADDR);
    sens  = GetSensSR850(ADDR);
    oflt  = GetOFLTSR850(ADDR);

    printf("Частота     : %.3f Гц\n",  freq);
    printf("Фаза        : %.3f °\n",   phase);
    printf("Чувств. код : %d\n",       sens);
    printf("Пост. врем. : %d\n",       oflt);

    /* ---------------------------------------------------------- */
    /* 3. Установка параметров                                      */
    /* ---------------------------------------------------------- */
    printf("\nУстановка частоты 1000.0 Гц...\n");
    SetFreqSR850(ADDR, 1000.0f);

    printf("Установка чувствительности (код 20 = 10 мВ)...\n");
    SetSensSR850(ADDR, 20);

    printf("Установка постоянной времени (код 8 = 100 мс)...\n");
    SetOFLTSR850(ADDR, 8);

    /* Дать прибору время стабилизироваться */
    sleep(1);

    /* ---------------------------------------------------------- */
    /* 4. Автоподстройка                                           */
    /* ---------------------------------------------------------- */
    printf("\nАвтоподстройка фазы...\n");
    AutoPhaseSR850(ADDR);
    sleep(1);

    /* ---------------------------------------------------------- */
    /* 5. Основные измерения                                        */
    /* ---------------------------------------------------------- */
    x     = GetXSR850(ADDR);
    y     = GetYSR850(ADDR);
    r     = GetRSR850(ADDR);
    theta = GetThetaSR850(ADDR);

    printf("\n--- Результаты измерений ---\n");
    printf("X     = %+.6f В\n", x);
    printf("Y     = %+.6f В\n", y);
    printf("R     = %.6f В\n",  r);
    printf("Theta = %+.3f °\n", theta);

    /* ---------------------------------------------------------- */
    /* 6. Курсор                                                   */
    /* ---------------------------------------------------------- */
    ret = GetCURSSR850(ADDR, 1, &cx, &cy);
    if (ret == 0)
        printf("\nКурсор 1: X=%.6f  Y=%.6f\n", cx, cy);
    else
        printf("\nОшибка чтения курсора.\n");

    /* ---------------------------------------------------------- */
    /* 7. Простой цикл измерений                                   */
    /* ---------------------------------------------------------- */
    printf("\n--- Цикл из 5 измерений (1 с интервал) ---\n");
    {
        int i;
        for (i = 0; i < 5; i++)
        {
            x = GetXSR850(ADDR);
            y = GetYSR850(ADDR);
            r = GetRSR850(ADDR);
            printf("[%d] X=%+.6f В  Y=%+.6f В  R=%.6f В\n",
                   i + 1, x, y, r);
            sleep(1);
        }
    }

    printf("\nГотово.\n");
    return 0;
}
